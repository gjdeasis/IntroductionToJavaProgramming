/*
 * Prompt: (Area and perimeter of a circle) Write a program that displays
 * the area and perimeter of a circle that has a radius of 5.5 using the following formula:
 * perimeter = 2 x radius x pi
 * area = radius x radius x pi
 */

package exercise;

public class ExerciseOneEight {
	public static void main (String[] args) {
		System.out.println(2 * 5.5 * 3.14);
		System.out.println(5.5 * 5.5 * 3.14);
	}
}
